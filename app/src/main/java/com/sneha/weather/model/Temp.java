
package com.sneha.weather.model;


public class Temp {

    private String day;
    private String min;
    private String max;
    private String night;
    private String eve;
    private String morn;

    /**
     * 
     * @return
     *     The day
     */
    public String getDay() {
        return day;
    }

    /**
     * 
     * @param day
     *     The day
     */
    public void setDay(String day) {
        this.day = day;
    }

    /**
     * 
     * @return
     *     The min
     */
    public String getMin() {
        return min;
    }

    /**
     * 
     * @param min
     *     The min
     */
    public void setMin(String min) {
        this.min = min;
    }

    /**
     * 
     * @return
     *     The max
     */
    public String getMax() {
        return max;
    }

    /**
     * 
     * @param max
     *     The max
     */
    public void setMax(String max) {
        this.max = max;
    }

    /**
     * 
     * @return
     *     The night
     */
    public String getNight() {
        return night;
    }

    /**
     * 
     * @param night
     *     The night
     */
    public void setNight(String night) {
        this.night = night;
    }

    /**
     * 
     * @return
     *     The eve
     */
    public String getEve() {
        return eve;
    }

    /**
     * 
     * @param eve
     *     The eve
     */
    public void setEve(String eve) {
        this.eve = eve;
    }

    /**
     * 
     * @return
     *     The morn
     */
    public String getMorn() {
        return morn;
    }

    /**
     * 
     * @param morn
     *     The morn
     */
    public void setMorn(String morn) {
        this.morn = morn;
    }

}
